    private void btnAbrirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAbrirActionPerformed
        // Abrimos la imagen
        this.enTamOriginal = ImageManager.openImage();
        calcularDimensionesLabels(this.enTamOriginal.getWidth(null), 
            this.enTamOriginal.getHeight(null));
        // Se escala la imagen
        this.original = this.enTamOriginal.getScaledInstance(this.x1, this.y1, TYPE_INT_RGB);
        this.efecto = this.original.getScaledInstance(this.x1, this.y1, TYPE_INT_RGB);
        // La original la mostramos en la izquierda
        this.lblOriginal.setIcon(new ImageIcon(this.original.getScaledInstance(
            this.x1, this.y1, Image.SCALE_DEFAULT)));
        
        // A la que se aplica el efecto a la derecha
        this.lblEfecto.setIcon(new ImageIcon(this.efecto.getScaledInstance(
            this.x1, this.y1, Image.SCALE_DEFAULT)));
        // Los slider los regresamos a 0
        this.sliderTemperatura.setValue(0);
        this.sliderIluminacion.setValue(0);
    }//GEN-LAST:event_btnAbrirActionPerformed

    private void sliderTemperaturaStateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_sliderTemperaturaStateChanged
        // La temperatura
        this.efecto = Traslacion.cambiarTemperatura(this.original, 
            this.sliderTemperatura.getValue());
        // La iluminación
        this.efecto = Traslacion.cambiarIluminacion(this.efecto, 
            this.sliderIluminacion.getValue());
        // Actualizamos en la pantalla
        this.lblEfecto.setIcon(new ImageIcon(this.efecto.getScaledInstance(
            this.x1, this.y1, Image.SCALE_DEFAULT)));
    }//GEN-LAST:event_sliderTemperaturaStateChanged

    private void sliderIluminacionStateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_sliderIluminacionStateChanged
        // La iluminación
        this.efecto = Traslacion.cambiarIluminacion(this.original, 
            this.sliderIluminacion.getValue());
        // La temperatura
        this.efecto = Traslacion.cambiarTemperatura(this.efecto, 
            this.sliderTemperatura.getValue());
        // Actualizamos en la pantalla
        this.lblEfecto.setIcon(new ImageIcon(this.efecto.getScaledInstance(
            this.x1, this.y1, Image.SCALE_DEFAULT)));
    }//GEN-LAST:event_sliderIluminacionStateChanged

    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // Guardar la imagen con el efecto
        if(this.efecto != null) {
            JFileChooser f = new JFileChooser();
            f.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY); 
            f.showSaveDialog(null);
            // Aplicamos la temperatura
            Image res = Traslacion.cambiarTemperatura(this.enTamOriginal, 
                this.sliderTemperatura.getValue());
            // La iluminación
            res = Traslacion.cambiarIluminacion(res, this.sliderIluminacion.getValue());
            //System.out.println(f.getSelectedFile());
            String path = f.getSelectedFile().getAbsolutePath();
            try {
                if(ImageManager.saveImage(path, res, "png"));
            }
            catch (IOException ex) {
                ex.printStackTrace();
            }
        }
        else System.out.println("Abre una imagen primero");
    }//GEN-LAST:event_btnGuardarActionPerformed

