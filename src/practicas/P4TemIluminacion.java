    private void calcularDimensionesLabels(int x1, int y1) {
        double p;
        
        if(y1 >= x1) {
            p = (double)y1/(double)x1;
            if(y1 > lblSize) {
                y1 = lblSize;
                x1 = (int)(y1 / p);
            }
        }
        else {
           p = (double)x1/(double)y1;
           if(x1 > lblSize) {
                x1 = lblSize;
                y1 = (int)(x1 / p);
            }
        }
        
        this.x1 = x1;
        this.y1 = y1;
    }

